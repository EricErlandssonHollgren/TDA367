package Interfaces;

public interface IObject {
    /**
     * Getter for x value
     * @return float
     */
    float getX();

    /**
     * getter for y value
     * @return float
     */
    float getY();

    /**
     * Getter for height
     * @return float
     */
    float getHeight();

    /**
     * Getter for width
     * @return float
     */
    float getWidth();

}
